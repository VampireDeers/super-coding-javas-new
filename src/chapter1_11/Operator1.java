package chapter1_11;

public class Operator1 {

    public static void main(String[] args){
        // 대입 연산자, 부호 연산자
        int c = -10;
        int b = c;
        int a = b;

        System.out.println(a);

        // 산술 연산자 + - * /
        int x = 10;
        int y;
        y = 10;
        int z = 2;

        int result = ( x + y ) / z;
        System.out.println(result);
    }
}
